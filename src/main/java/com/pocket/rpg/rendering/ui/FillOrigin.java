package com.pocket.rpg.rendering.ui;

/**
 * Origin point for fill direction.
 */
public enum FillOrigin {
    // Horizontal origins
    LEFT,
    RIGHT,
    // Vertical origins
    BOTTOM,
    TOP,
    // Radial origins (corner or edge to start from)
    BOTTOM_LEFT,
    TOP_LEFT,
    TOP_RIGHT,
    BOTTOM_RIGHT
}
