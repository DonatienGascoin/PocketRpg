package com.pocket.rpg.rendering.ui;

/**
 * Fill method for FILLED image type.
 */
public enum FillMethod {
    /** Fill horizontally */
    HORIZONTAL,
    /** Fill vertically */
    VERTICAL,
    /** Fill radially in a 90-degree arc */
    RADIAL_90,
    /** Fill radially in a 180-degree arc */
    RADIAL_180,
    /** Fill radially in a full 360-degree circle */
    RADIAL_360
}
